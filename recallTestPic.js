$(function() {
    $("#btnHint1").click(function(){
        $("#btnHint1").hide("slow");
        $("#pic1").show("slow");
        $("p#A1").show("slow");
        $("#btnTrue1").show("slow");
        $("#btnFalse1").show("slow");
    });
});
$(function() {
    $("#btnTrue1").click(function(){
        $("#zone1").css("background-color","green");
        $("#btnTrue1").hide("slow");
        $("#btnFalse1").hide("slow");
    });
});
$(function() {
    $("#btnFalse1").click(function(){
        $("#zone1").css("background-color","red");
        $("#btnTrue1").hide("slow");
        $("#btnFalse1").hide("slow");
    });
});

